interface PredictionResultProps {
  price: number | null;
  error: string | null;
}

export default function PredictionResult({ price, error }: PredictionResultProps) {
  if (!price && !error) return null;

  if (error) {
    return (
      <div className="mt-6 rounded-xl border border-red-100 bg-red-50 px-4 py-3 text-sm text-red-600">
        {error}
      </div>
    );
  }

  return (
    <div className="mt-6 rounded-xl border border-indigo-100 bg-indigo-50 px-4 py-4 text-center">
      <p className="text-sm font-medium text-gray-500">Predicted House Price</p>
      <p className="mt-1 text-2xl font-bold text-indigo-700">
        {price?.toLocaleString(undefined, {
          style: "currency",
          currency: "ETB",
          maximumFractionDigits: 0,
        })}
      </p>
      <p className="mt-3 text-xs leading-relaxed text-gray-500">
        This estimate is generated by a machine learning model trained on
        historical property data. It reflects a statistical approximation
        based on the details you provided — rooms, area, location, and
        construction type — and may differ from an official appraisal or
        market valuation.
      </p>
    </div>
  );
}